package chap4;

public class ForTest {

	public static void main(String[] args) {
		// 1 - 10 모든 자연수 총합 출력
		int sum = 0;
		for(int i = 1; i <= 10; i++ ) {
			sum = sum + i;
		//	System.out.println("i=" + i + " , sum=" + sum);
		}

		System.out.println(sum);
		
		
	}

}







